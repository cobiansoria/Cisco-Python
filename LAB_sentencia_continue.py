# Indicar al usuario que ingrese una palabra
# y asignarlo a la variable user_word.
word = input("Ingresa una palabra para el devorador: ")
word = word.upper()
for letter in word:
    # Completa el cuerpo del bucle for.
    if letter == 'A':
        continue
    elif letter == 'E':
        continue 
    elif letter == 'I':
        continue 
    elif letter == 'O': 
        continue 
    elif letter == 'U':
        continue
    print(letter)